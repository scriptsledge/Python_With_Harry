# Arithmetic Operators
a = 3
b = 4
c = a + b  # Addition
print(c)   # Output: 7

# Assignment Operators
a = 30 - 10  # Assign the result of 30 - 10 to 'a'
print(a)     # Output: 20

b = 0
b += 5       # Increment 'b' by 5 (b = b + 5)
b -= 4       # Decrement 'b' by 4 (b = b - 4)
b *= 5       # Multiply 'b' by 5 (b = b * 5)
b /= 2       # Divide 'b' by 2 (b = b / 2)
print(b)     # Output: 2.5

# Comparison Operators
d = 5 < 45   # Less than comparison
print(d)     # Output: True

d = 5 > 45   # Greater than comparison
print(d)     # Output: False

d = 5 <= 5   # Less than or equal to comparison
print(d)     # Output: True

d = 5 != 5   # Not equal comparison
print(d)     # Output: False

d = 5 == 5   # Equal comparison
print(d)     # Output: True

# Logical Operators
e = True or False  # Logical OR
print(e)           # Output: True

# Truth table of 'or'
print("True or False is ", True or False)   # Output: True
print("True or True is ", True or True)     # Output: True
print("False or True is ", False or True)   # Output: True
print("False or False is ", False or False) # Output: False

# Truth table of 'and'
print("True and False is ", True and False)   # Output: False
print("True and True is ", True and True)     # Output: True
print("False and True is ", False and True)   # Output: False
print("False and False is ", False and False) # Output: False

# Truth table of 'not'
print(not(True))   # Output: False
print(not(False))  # Output: True
