a = int(input("Enter your age:"))

# if-else conditional to check age
if a >= 18:  # Checks if age is 18 or more
    print("You are above the age of consent")
    print("Good for you")
else:  # Runs if age is below 18
    print("You are below the age of consent")

print("End of program!")  # Runs after the conditionals
