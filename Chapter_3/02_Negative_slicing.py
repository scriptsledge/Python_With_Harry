name = "Carry"

# Slicing examples
print(name[0:3])  # Output: 'Car' - from index 0 to 2 (excluding 3)
print(name[-4:-1])  # Output: 'arr' - from index -4 to -2 (excluding -1)
print(name[1:4])  # Output: 'arr' - from index 1 to 3 (excluding 4)
print(name[:4])  # Output: 'Carr' - from index 0 to 3 (same as name[0:4])
print(name[1:])  # Output: 'arry' - from index 1 to end (same as name[1:5])
