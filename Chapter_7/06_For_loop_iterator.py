# for loop iterator - iterates over the elements of a list, tuple, or string
lisst = [1, 2, 3, 4, 5]
for i in lisst:  # Loops through each element in the list
    print(i)  # Prints each element

tupal = (7, 77, 777, 7777)
for i in tupal:  # Loops through each element in the tuple
    print(i)  # Prints each element

streeing = "Script Sledge"
for i in streeing:  # Loops through each character in the string
    print(i)  # Prints each character
