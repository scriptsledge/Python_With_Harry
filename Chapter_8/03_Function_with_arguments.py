# Functions with arguments and return value
def greet(naam, anant):
    print("Good Day,", naam)
    print(anant)
    return "Done"  # Returns "Done" after execution

# Calling the function with different arguments
greet("Sab", "Thankkkk")
greet("Yom", "Youuuu")

# Storing the return value in a variable and printing it
a = greet("Wlu", "Dhanyabaad")
print(a)
