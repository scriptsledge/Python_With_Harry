# **Virtual Environment and Pip Freeze**

# Create and use isolated environments for Python projects
# Install and activate `virtualenv`:
# 1. Install: `pip install virtualenv`
# 2. Create env: `virtualenv env_name`
# 3. Activate: `source env_name/bin/activate` (Linux/Mac) or `.\env_name\Scripts\activate` (Windows)

# Install dependencies using pip
# Example:
# pip install requests flask

# List installed packages and save to a file
# Command: `pip freeze > requirements.txt`
# Output example:
# Flask==2.1.1
# requests==2.26.0

# To install from the requirements file:
# Command: `pip install -r requirements.txt`

