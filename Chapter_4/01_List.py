# Lists: ordered, mutable collections that can store elements of any data type
friends = ["Apple", 100, 738.33, True, 'z']
print(friends[0])

# Lists allow modification of elements
friends[0] = "Grapes"
print(friends[0])

# List slicing: accesses elements from index 1 to 3 (4 is excluded)
print(friends[1:4])
